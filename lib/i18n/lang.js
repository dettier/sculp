////////////////////////////////////////////////////////////////////////////////
// REQUIRES : BEGIN
////////////////////////////////////////////////////////////////////////////////

import en from './languages/en';
import ru from './languages/ru';

////////////////////////////////////////////////////////////////////////////////
// REQUIRES : END
////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
// current messages
////////////////////////////////////////////////////////////////////////////////

// eslint-disable-next-line import/no-mutable-exports
export let messages = en;

////////////////////////////////////////////////////////////////////////////////
// lang
////////////////////////////////////////////////////////////////////////////////

export function lang (newMessages) {
  if (newMessages === 'ru')
    newMessages = ru;
  else if (newMessages === 'en')
    newMessages = en;

  messages = {
    ...messages,
    ...newMessages
  };
}
